def full_feature_engineering_MORE(df):
    df = df.copy()

    # Basic Ratios
    df['header_per_time'] = df['header_size'] / (df['flow_time'] + 1e-6)
    df['packet_per_rate'] = df['packet_duration'] / (df['overall_rate'] + 1e-6)
    df['header_per_packet'] = df['header_size'] / (df['packet_duration'] + 1e-6)
    df['packet_per_time'] = df['packet_duration'] / (df['flow_time'] + 1e-6)
    df['rate_per_header'] = df['overall_rate'] / (df['header_size'] + 1e-6)
    df['max_per_cov'] = df['max_value'] / (df['value_covariance'] + 1e-6)

    # Flags
    flag_cols = ['fin_flags', 'syn_flags', 'rst_flags', 'psh_flags', 'ack_flags']
    df['total_flags'] = df[flag_cols].sum(axis=1)
    df['fin_ratio'] = df['fin_flags'] / (df['total_flags'] + 1e-6)
    df['flags_ratio'] = df['syn_flags'] / (df['ack_flags'] + 1e-6)
    df['is_syn_ack'] = ((df['syn_flags'] > 0) & (df['ack_flags'] > 0)).astype(int)

    # Protocols
    protocol_cols = ['protocol_http', 'protocol_https', 'protocol_tcp', 'protocol_udp', 'protocol_icmp']
    df['protocol_count'] = df[protocol_cols].sum(axis=1)
    df['tcp_udp_ratio'] = (df['protocol_tcp'] + 1e-6) / (df['protocol_udp'] + 1e-6)
    df['http_https_ratio'] = (df['protocol_http'] + 1e-6) / (df['protocol_https'] + 1e-6)

    # Interactions
    df['flow_rate_interaction'] = df['flow_time'] * df['overall_rate']
    df['header_max_interaction'] = df['header_size'] * df['max_value']
    df['flow_max_interact'] = df['flow_time'] * df['max_value']
    df['header_cov_interact'] = df['header_size'] * df['value_covariance']
    df['packet_time_interact'] = df['packet_duration'] * df['flow_time']
    df['rate_sq'] = df['overall_rate'] ** 2

    # Transforms
    for col in ['flow_time', 'header_size', 'packet_duration', 'overall_rate', 'max_value', 'value_covariance']:
        df[f'log_{col}'] = np.log1p(df[col])
        df[f'sqrt_{col}'] = np.sqrt(df[col])


    # Flag Interactions
    df['syn_fin_combo'] = df['syn_flags'] * df['fin_flags']
    df['ack_psh_combo'] = df['ack_flags'] * df['psh_flags']

    # Protocol + Flag Interaction
    df['tcp_fin_ack'] = df['protocol_tcp'] * df['fin_flags'] * df['ack_flags']

    # Binned Indicators (Binary flags for outliers)
    df['is_long_flow'] = (df['flow_time'] > df['flow_time'].median()).astype(int)
    df['is_high_rate'] = (df['overall_rate'] > df['overall_rate'].quantile(0.75)).astype(int)

    # More Flags
    df['syn_to_ack_ratio'] = df['syn_flags'] / (df['ack_flags'] + 1)
    df['fin_to_rst_ratio'] = df['fin_flags'] / (df['rst_flags'] + 1)
    df['ack_psh_to_total_ratio'] = (df['ack_flags'] + df['psh_flags']) / (df['total_flags'] + 1)

    # Attack separators for Better Recon, DoS, Mirai Detection
    df['rst_ratio'] = df['rst_flags'] / (df['total_flags'] + 1e-6)
    df['icmp_ratio'] = df['protocol_icmp'] / (df['protocol_count'] + 1e-6)
    df['is_small_packet'] = (df['header_size'] < df['header_size'].quantile(0.25)).astype(int)
    df['is_syn_no_ack'] = ((df['syn_flags'] > 0) & (df['ack_flags'] == 0)).astype(int)

    return df

