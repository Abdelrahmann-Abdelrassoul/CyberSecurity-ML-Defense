# Setup
n_splits = 5
cv = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)
n_classes = len(np.unique(y_train))
n_features = X_train.shape[1]

# XGBoost Params
xgb_params = {
    'max_depth': 10,
    'learning_rate': 0.025,
    'subsample': 0.7,
    'colsample_bytree': 0.7,
    'objective': 'multi:softprob',
    'num_class': n_classes,
    'random_state': 42,
    'reg_alpha': 2,
    'reg_lambda': 2,
    'tree_method': 'hist',
    'eval_metric': ['mlogloss', 'merror']
}

# Cross-validated Training
xgb_oof_preds = np.zeros((X_train.shape[0], n_classes))

for fold, (train_idx, val_idx) in enumerate(cv.split(X_train, y_train)):
    print(f"\nFold {fold + 1}/{n_splits}")

    dtrain = xgb.DMatrix(X_train.iloc[train_idx], label=y_train.iloc[train_idx])
    dval = xgb.DMatrix(X_train.iloc[val_idx], label=y_train.iloc[val_idx])

    evals = [(dtrain, 'train'), (dval, 'eval')]

    model = xgb.train(
        params=xgb_params,
        dtrain=dtrain,
        num_boost_round=800, # EXTRA
        evals=evals,
        early_stopping_rounds=20,
        verbose_eval=10
    )

    xgb_oof_preds[val_idx] = model.predict(dval)

# CV Score
xgb_cv_score = accuracy_score(y_train, np.argmax(xgb_oof_preds, axis=1))
print(f"\n XGBoost CV Accuracy: {xgb_cv_score:.4f}")

